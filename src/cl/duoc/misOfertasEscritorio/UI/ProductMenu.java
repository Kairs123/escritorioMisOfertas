    private void jButtonAddProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAddProductMouseClicked
        ProductoDAO pDAO = new ProductoDAO();
        RubroDAO rDAO = new RubroDAO();
        Producto p = new Producto();
        Long cod;
        try {
            if (verificarCampo(jTextFieldAddNombre, 100)) {
                cod = pDAO.getMaxId();
                p.setIdProducto(++cod);
                p.setNombreProducto(jTextFieldAddNombre.getText());
                p.setRubroId(rDAO.findByName(jCbxRubro.getSelectedItem().toString()));
                p.setIsActive("1");
                if (!jCheckBoxPerecible.isSelected()) {
                    p.setEsPerecible(1);
                    p.setFechaVencimiento("N/A");
                } else {
                    p.setEsPerecible(2);
                    Date today = new Date();
                    if (jDateChooserAddProducto.getDate().after(today) || jDateChooserAddProducto.equals(today)) {
                        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                        String strDate = dateFormat.format(jDateChooserAddProducto.getDate());
                        p.setFechaVencimiento(strDate);
                        pDAO.addProducto(p);
                        JOptionPane.showMessageDialog(null, "Producto con ID: " + p.getIdProducto() + " agregado con exito!");
                        clearTable();
                    } else {
                        JOptionPane.showMessageDialog(null, "Está intentando ingresar un producto vencido.");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe ingresar valor en campo nombre. Largo permitido entre 1 y 100");
                jTextFieldAddNombre.requestFocus();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonAddProductMouseClicked

