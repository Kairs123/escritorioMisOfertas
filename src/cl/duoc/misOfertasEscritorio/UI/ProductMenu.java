    private void populateTable() {
        ProductoDAO pDAO = new ProductoDAO();
        DefaultTableModel model = (DefaultTableModel) jTableProducto.getModel();
        List<Producto> lista;
        lista = pDAO.listAll();
        Object rowData[] = new Object[5];
        for (Producto prod : lista) {
            rowData[0] = prod.getIdProducto();
            rowData[1] = prod.getNombreProducto();
            rowData[2] = prod.getRubroId().getNombreRubro();
            rowData[3] = prod.getFechaVencimiento();
            rowData[4] = prod.getIsActive();
            model.addRow(rowData);
        }
    }

    private void populateComboBox() {
        RubroDAO rDAO = new RubroDAO();
        List<Rubro> lista = rDAO.listAll();
        jCbxRubro.removeAllItems();
        DefaultComboBoxModel defaultC = new DefaultComboBoxModel();
        for (Rubro r : lista) {
            defaultC.addElement(r.getNombreRubro());
        }
        jCbxRubro.setModel(defaultC);
    }

    private void clearTable() {
        DefaultTableModel dtm = (DefaultTableModel) jTableProducto.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        jTableProducto.setModel(dtm);
        populateTable();
    }

    /**
     *
     * @param {JTexfield} j - JTexfield que debe ser validado
     * @param {Integer} largo - Largo a validar para j
     * @return
     */
    private boolean verificarCampo(JTextField j, int largo) {
        int largoText;
        if (validarVacio(j)) {
            largoText = j.getText().length();
            if (largoText < largo) {
                return true;
            } else {
                return false;
            }
        } else {
            return false;
        }

    }

    /**
     *
     * @param j
     * @return
     */
    private boolean validarVacio(JTextField j) {
        if (j.getText().trim().length() > 0) {
            return true;
        } else {
            return false;
        }
    }

    private void setHeaderColor() {
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(36, 47, 65));
        headerRenderer.setForeground(Color.WHITE);
        headerRenderer.setBorder(BorderFactory.createEmptyBorder());
        for (int i = 0; i < jTableProducto.getModel().getColumnCount(); i++) {
            jTableProducto.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
        }
    }

    /**
     *
     * @param jtable
     * @throws FileNotFoundException
     * @throws DocumentException
     */
    private void exportPDF(JTable jtable) throws FileNotFoundException, DocumentException {
        Document document = new Document();
        PdfWriter writer;
        //URL destino
        writer = PdfWriter.getInstance(document, new FileOutputStream("reporteProductos.pdf"));
        document.open();
        PdfContentByte cb = writer.getDirectContent();
        PdfTemplate tp = cb.createTemplate(500, 500);
        Graphics2D g2;
        //Imprimir documento
        g2 = tp.createGraphicsShapes(500, 500);
        jtable.print(g2);
        g2.dispose();
        cb.addTemplate(tp, 30, 300);

        // Cerrar documento
        document.close();
    }

